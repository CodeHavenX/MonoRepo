package com.cramsan.edifikana.client.ui.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Standard spacing values used throughout the Edifikana UI.
 */
object Spacing {
    /** Spacing between tightly-related items within a card or row. */
    val xs: Dp = 4.dp

    /** Spacing between items in a list or section. */
    val sm: Dp = 8.dp

    /** Spacing between grouped elements within a card row. */
    val md: Dp = 12.dp

    /** Standard content horizontal padding and card internal padding. */
    val lg: Dp = 16.dp

    /** Spacing between major sections or header elements. */
    val xl: Dp = 24.dp
}
