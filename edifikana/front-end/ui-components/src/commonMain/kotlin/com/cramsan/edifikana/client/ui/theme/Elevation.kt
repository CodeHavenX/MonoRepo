package com.cramsan.edifikana.client.ui.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Standard elevation values used throughout the Edifikana UI.
 */
object Elevation {
    /** Resting elevation for an interactive surface, e.g. a clickable card. */
    val resting: Dp = 3.dp

    /** Elevation while a pointer is hovering over an interactive surface. */
    val hovered: Dp = 8.dp

    /** Elevation while an interactive surface is being pressed. */
    val pressed: Dp = 1.dp
}
